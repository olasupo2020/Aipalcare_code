export function SessionNotes(
    clientName, 
    dateOfSession,
    dateOfBirth,
    age,
    dateOfInitialEval,
    treatmentPeriod,
    pryDiagnosis,
    cptTreatmentCode,
    parentCaregiverName
    ){
    this.clientName = clientName;
    this.dateOfSession = dateOfSession;
    this.dateOfBirth = dateOfBirth;
    this.age = age;
    this.dateOfInitialEval = dateOfInitialEval;
    this.treatmentPeriod = treatmentPeriod;
    this.pryDiagnosis = pryDiagnosis;
    this.cptTreatmentCode = cptTreatmentCode;
    this.parentCaregiverName = parentCaregiverName;

}